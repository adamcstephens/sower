defmodule SowerClient.Schemas.Orchestration.DeploymentResult do
  use SowerClient.Schema

  OpenApiSpex.schema(%{
    title: "DeploymentResult",
    type: :object,
    properties: %{
      request_id: %Schema{
        type: :string,
        description: "request sid, autogenerated at deployment request"
      },
      deployment_sid: %Schema{
        type: :string,
        description: "deployment sid which is being reported on"
      },
      result: %Schema{
        type: :string,
        description: "result of the deployment",
        enum: [:success, :failure, :partial]
      },
      output: %Schema{
        type: :array,
        items: %Schema{
          type: :string,
          description: "subscription sid allocated by Sower"
        },
        default: []
      },
      deployed_at: %Schema{
        type: :string,
        format: :date_time,
        description: "when the deployment was deployed",
        default: DateTime.from_unix!(0) |> DateTime.to_iso8601()
      }
    },
    required: [:request_id, :deployment_sid, :result]
  })
end
